
<?php

CREATE TABLE IF NOT EXISTS
 STUDENT(firstname TEXT NOT NULL, lastname TEXT NOT NULL,
  email VARCHAR(255) NOT NULL,mobile TEXT NOT NULL,enrollment VARCHAR(255) NOT NULL,
  cgpa FLOAT NOT NULL,passingyear TEXT NOT NULL,branch TEXT NOT NULL,year TEXT NOT NULL,division INT NOT NULL,age INT NOT NULL,PRIMARY KEY(enrollment));


CREATE TABLE IF NOT EXISTS
STUDENTLOGIN(enrollment VARCHAR(255) NOT NULL,
username VARCHAR(255) NOT NULL,
password VARCHAR(255) NOT NULL,
FOREIGN KEY(enrollment) REFERENCES STUDENT(enrollment) ON DELETE CASCADE);

CREATE TABLE IF NOT EXISTS ADMIN(facultyid VARCHAR(255) NOT NULL PRIMARY KEY,
    password VARCHAR(255) NOT NULL);



CREATE TABLE IF NOT EXISTS 
INTENNSHIPINFO(jobid VARCHAR(255) NOT NULL PRIMARY KEY,jobname VARCHAR(255) NOT NULL,companyname VARCHAR(255) NOT NULL,dateposted DATETIME NOT NULL,location VARCHAR(255) NOT NULL,duration INT NOT NULL,stipend INT NOT NULL,lastdate DATETIME NOT NULL,cgpa FLOAT NOT NULL,passingyear VARCHAR(255) NOT NULL,description VARCHAR(30000) NOT NULL);



